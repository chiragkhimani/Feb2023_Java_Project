package com.java.class04;
// Write a program to print greatest number out of two numbers

public class FindGreatestNumberOutOfTwoNumbers {
	public static void main(String[] args) {
		
		int num1 = 120, num2 = 450;
		
		if(num1 > num2) {
			System.out.println(num1 + " is greater");
		}else {
			System.out.println(num2 + " is greater");
		}
		
	}
}

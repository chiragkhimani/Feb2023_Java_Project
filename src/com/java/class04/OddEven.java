package com.java.class04;

// Write a program to check given number is odd or even
public class OddEven {
	public static void main(String[] args) {
		// input
		int num1 = 132; // Data
		
		// Code (Logic)
		if(num1 % 2 == 0) {
			System.out.println("even");
		}else {
			System.out.println("odd");
		}
		
	}
}
